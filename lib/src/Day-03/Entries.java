public class Entries {

    private String entry;
    private String date;

    public Entries(String entry, String date) {
        this.entry = entry;
        this.date = date;
    }

    public String getEntry() {
        return entry;
    }

    public void setEntry() {
        this.entry = entry;
    }

    public String getDate() {
        return date;
    }

    public void setDate() {
        this.date = date;
    }
}
